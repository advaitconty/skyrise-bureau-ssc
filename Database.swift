    // Helper functions
    func aircraft(byCode code: String) -> Aircraft? {
        allAircraft.first { $0.modelCode == code }
    }
    
    func aircraft(byCategory category: AircraftCategory) -> [Aircraft] {
        allAircraft.filter { $0.category == category }
    }
    
    func aircraft(byManufacturer manufacturer: AircraftManufacturer) -> [Aircraft] {
        allAircraft.filter { $0.manufacturer == manufacturer }
    }
    
    func endgameAircraft() -> [Aircraft] {
        allAircraft.filter { $0.isEndgame }
    }
    
    func supersonicAircraft() -> [Aircraft] {
        allAircraft.filter { $0.isSupersonic }
    }
    
    func affordableAircraft(budget: Double) -> [Aircraft] {
        allAircraft.filter { $0.purchasePrice <= budget }
    }
    
    func aircraftForRoute(distance: Int, runwayLength: Int) -> [Aircraft] {
        allAircraft.filter { aircraft in
            aircraft.maxRange >= distance && aircraft.minRunwayLength <= runwayLength
        }
    }
