    // Helper functions
    func airport(byCode code: String) -> Airport? {
        allAirports.first { $0.iata == code || $0.icao == code }
    }
    
    func airports(byRegion region: Region) -> [Airport] {
        allAirports.filter { $0.region == region }
    }
    
    func airports(byCountry country: String) -> [Airport] {
        allAirports.filter { $0.country == country }
    }
    
    func majorHubs() -> [Airport] {
        allAirports.filter { $0.demand.passengerDemand >= 9.0 }
    }
    
    func calculateDistance(from: Airport, to: Airport) -> Int {
        // Haversine formula for great circle distance
        let earthRadius = 6371.0 // km
        let lat1 = from.latitude * .pi / 180
        let lat2 = to.latitude * .pi / 180
        let dLat = (to.latitude - from.latitude) * .pi / 180
        let dLon = (to.longitude - from.longitude) * .pi / 180
        
        let a = sin(dLat/2) * sin(dLat/2) +
        cos(lat1) * cos(lat2) *
        sin(dLon/2) * sin(dLon/2)
        let c = 2 * atan2(sqrt(a), sqrt(1-a))
        
        return Int(earthRadius * c)
    }
